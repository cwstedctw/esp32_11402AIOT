def draw_char(display, char, x, y, color=1):
    rows = GLYPHS.get(char)
    if rows is None:
        raise ValueError("missing glyph: " + char)

    for row_index, row in enumerate(rows):
        for col_index, pixel in enumerate(row):
            if pixel == "1":
                display.pixel(x + col_index, y + row_index, color)


def draw_text(display, text, x, y, spacing=1, color=1):
    cursor_x = x
    for char in text:
        draw_char(display, char, cursor_x, y, color)
        cursor_x += WIDTH + spacing


def text_width(text, spacing=1):
    if not text:
        return 0
    return len(text) * WIDTH + (len(text) - 1) * spacing
